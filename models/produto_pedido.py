
class ProdutoPedido:

    def __init__(self):
        pass

    def getIdProdutoPedido(self):
        return self.__IdProdutoPedido

    def setIdProdutoPedido(self, IdProdutoPedido):
        self.__IdProdutoPedido = IdProdutoPedido

    def getIdProduto(self):
        return self.__IdProduto

    def setIdProduto(self, IdProduto):
        self.__IdProduto = IdProduto

    def getIdPedido(self):
        return self.__IdPedido

    def setIdPedido(self, IdPedido):
        self.__IdPedido = IdPedido

    def getQuantidade(self):
        return self.__Quantidade

    def setQuantidade(self, Quantidade):
        self.__Quantidade = Quantidade
