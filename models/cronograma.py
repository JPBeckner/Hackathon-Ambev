
class Cronograma:

    def __init__(self):
        pass

    def getIdCronograma(self):
        return self.__IdCronograma

    def setIdCronograma(self, IdCronograma):
        self.__IdCronograma = IdCronograma    

    def getQuantidade(self):
        return self.__Quantidade

    def setQuantidade(self, Quantidade):
        self.__Quantidade = Quantidade  

    def getDataSaida(self):
        return self.__DataSaida

    def setDataSaida(self, DataSaida):
        self.__DataSaida = DataSaida
        
    def getDataEntrega(self):
        return self.__DataEntrega

    def setDataEntrega(self, DataEntrega):
        self.__DataEntrega = DataEntrega

    def getIdTransporte(self):
        return self.__IdTransporte

    def setIdTransporte(self, IdTransporte):
        self.__IdTransporte = IdTransporte

    def getIdLocalEntrega(self):
        return self.__IdLocalEntrega

    def setIdLocalEntrega(self, IdLocalEntrega):
        self.__IdLocalEntrega = IdLocalEntrega

    def getIdPedido(self):
        return self.__IdPedido

    def setIdPedido(self, IdPedido):
        self.__IdPedido = IdPedido                               
